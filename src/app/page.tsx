export default function AuthPage() {
  const { user, loading: authLoading } = useAuth();
  const router = useRouter();
  const { toast } = useToast();

  const [loading, setLoading] = useState(false);

  if (authLoading) {
    return (
      <div className="flex h-screen w-screen items-center justify-center">
        <Loader2 className="h-12 w-12 animate-spin text-primary" />
      </div>
    );
  }

  if (user) {
    router.replace('/dashboard');
    return null;
  }

  const handleAuthError = (err: AuthError) => {
    console.error(err);
    switch (err.code) {
      case 'auth/popup-closed-by-user':
        return text.auth.errors.popupClosed;
      default:
        return text.auth.errors.unexpected;
    }
  };

  const handleGoogleSignIn = async () => {
    setLoading(true);
    try {
      const provider = new GoogleAuthProvider();
      await signInWithPopup(auth, provider);
      router.push('/dashboard');
    } catch (err) {
      const errorMessage = handleAuthError(err as AuthError);
      toast({
        variant: 'destructive',
        title: text.auth.googleSignInFailed,
        description: errorMessage,
      });
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="flex flex-col items-center justify-center gap-12">
       <div className="flex items-center space-x-2">
            <Wallet className="h-10 w-10 text-primary" />
            <span className="text-4xl font-bold font-headline">
              A Jornada
            </span>
        </div>
      <Card className="w-full max-w-sm">
        <CardHeader className="text-center">
          <CardTitle>Bem-vindo</CardTitle>
        </CardHeader>
        <CardContent>
          <Button
            variant="default"
            className="w-full"
            onClick={handleGoogleSignIn}
            disabled={loading}
          >
            {loading ? (
              <Loader2 className="mr-2 h-4 w-4 animate-spin" />
            ) : (
              <GoogleIcon className="mr-2" />
            )}
            Entrar com Google
          </Button>
        </CardContent>
      </Card>
    </div>
  );
}
